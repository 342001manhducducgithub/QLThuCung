    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        try{
         //lấy thông tin của User
            String username=txtusername.getText();
            String password=new String(txtpassword.getPassword());
            //Tạo ra đối tượng giao tiếp CSDL
            DBAccess acc=new DBAccess();
            ResultSet rs=acc.Query("Select * from TaiKhoan where tenDangNhap='"+username+"' and matKhau='"+password+"'");
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Đăng nhập thành công!");
                frmMain obj = new frmMain();// cap phat bộ nhớ cho form DK
                obj.setVisible(true);//Hien thi frm dang ky len man hinh
            }else
                JOptionPane.showMessageDialog(null, "Đăng nhập thất bại!");
        }catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

